CREATE MEMORY TABLE PUBLIC.EMPLOYEES (
  ID         INTEGER GENERATED BY DEFAULT AS IDENTITY (
  START WITH 0 ) NOT NULL PRIMARY KEY,
  NAME       VARCHAR(30),
  SURNAME    VARCHAR(30),
  PATRONYMIC VARCHAR(30),
  POSITION   VARCHAR(30)
);
CREATE MEMORY TABLE PUBLIC.PROJECTS (
  ID           INTEGER GENERATED BY DEFAULT AS IDENTITY (
  START WITH 0 ) NOT NULL PRIMARY KEY,
  NAME         VARCHAR(30),
  ABBREVIATION VARCHAR(30),
  DESCRIPTION  VARCHAR(100)
);
CREATE MEMORY TABLE PUBLIC.REFLIST_EMPL (
  ID          INTEGER GENERATED BY DEFAULT AS IDENTITY (
  START WITH 0 ) NOT NULL PRIMARY KEY,
  ID_EMPLOYEE INTEGER,
  ID_TASK     INTEGER
);
CREATE MEMORY TABLE PUBLIC.REFLIST_PROJ (
  ID         INTEGER GENERATED BY DEFAULT AS IDENTITY (
  START WITH 0 ) NOT NULL PRIMARY KEY,
  ID_PROJECT INTEGER,
  ID_TASK    INTEGER
);
CREATE MEMORY TABLE PUBLIC.TASKS (
  ID          INTEGER GENERATED BY DEFAULT AS IDENTITY (
  START WITH 0 ) NOT NULL PRIMARY KEY,
  NAME        VARCHAR(30),
  DURATION    INTEGER,
  STARTDATE   DATE,
  ENDDATE     DATE,
  STATE       VARCHAR(30),
  PROJECTNAME VARCHAR(30)
);
