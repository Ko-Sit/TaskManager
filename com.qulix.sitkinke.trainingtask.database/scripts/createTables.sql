CREATE MEMORY TABLE PUBLIC.EMPLOYEES (
  ID         INTEGER GENERATED BY DEFAULT AS IDENTITY (
  START WITH 0 ) NOT NULL PRIMARY KEY,
  NAME       VARCHAR(100),
  SURNAME    VARCHAR(100),
  PATRONYMIC VARCHAR(100),
  POSITION   VARCHAR(100),
  EMAIL      VARCHAR(100),
  PASSWORD   VARCHAR(100),
  USERTYPE   VARCHAR(100)
);
CREATE MEMORY TABLE PUBLIC.PROJECTS (
  ID           INTEGER GENERATED BY DEFAULT AS IDENTITY (
  START WITH 0 ) NOT NULL PRIMARY KEY,
  NAME         VARCHAR(100),
  ABBREVIATION VARCHAR(100),
  DESCRIPTION  VARCHAR(200)
);
CREATE MEMORY TABLE PUBLIC.REFLIST_EMPL (
  ID          INTEGER GENERATED BY DEFAULT AS IDENTITY (
  START WITH 0 ) NOT NULL PRIMARY KEY,
  ID_EMPLOYEE INTEGER,
  ID_TASK     INTEGER
);
CREATE MEMORY TABLE PUBLIC.REFLIST_PROJ (
  ID         INTEGER GENERATED BY DEFAULT AS IDENTITY (
  START WITH 0 ) NOT NULL PRIMARY KEY,
  ID_PROJECT INTEGER,
  ID_TASK    INTEGER
);
CREATE MEMORY TABLE PUBLIC.TASKS (
  ID          INTEGER GENERATED BY DEFAULT AS IDENTITY (
  START WITH 0 ) NOT NULL PRIMARY KEY,
  NAME        VARCHAR(100),
  DURATION    INTEGER,
  STARTDATE   DATE,
  ENDDATE     DATE,
  STATE       VARCHAR(100),
  PROJECTNAME VARCHAR(100)
);
